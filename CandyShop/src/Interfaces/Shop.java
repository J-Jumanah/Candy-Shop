    private void buyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buyActionPerformed
        if (type.getSelectedItem() == "Chocolate") {
            //To store the type of chocolate a user will choose.
            candyType = "Chocolate";
            if (candy1.isSelected()) {
                //To store the flavor, price and quantity of the selected item.
                candyFlavour = flavor1.getText();
                candyPrice = 80;
                String getData = quantity1.getSelectedItem().toString();
                quantity = Integer.parseInt(getData);
                //To move to the payemnt interface.
//                Payment.pay.setVisible(true);
            } else if (candy2.isSelected()) {
                //To store the flavor, price and quantity of the selected item.
                candyFlavour = flavor2.getText();
                candyPrice = 75;
                String getData = quantity2.getSelectedItem().toString();
                quantity = Integer.parseInt(getData);
                //To move to the payemnt interface.
//                Payment.pay.setVisible(true);
            }

        } else if (type.getSelectedItem() == "Gummy Candy") {
            //To store the type of chocolate a user will choose.
            candyType = "Gummy Candy";
            if (candy1.isSelected()) {
                //To store the flavor, price and quantity of the selected item.
                candyFlavour = flavor1.getText();
                candyPrice = 36;
                String getData = quantity1.getSelectedItem().toString();
                quantity = Integer.parseInt(getData);
                //To move to the payemnt interface.
//                Payment.pay.setVisible(true);
            } else if (candy2.isSelected()) {
                //To store the flavor, price and quantity of the selected item.
                candyFlavour = flavor2.getText();
                candyPrice = 34;
                String getData = quantity2.getSelectedItem().toString();
                quantity = Integer.parseInt(getData);
                //To move to the payemnt interface.
//                Payment.pay.setVisible(true);
//                this.setVisible(false);
            }
        } else {
            //A popup msg will appear if no slection was made.
            JOptionPane.showMessageDialog(this, "You haven't chose any candy!");
        }
        //To move to the payemnt interface.
        Payment.pay.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_buyActionPerformed

