    private void typeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_typeActionPerformed
        try {
            BufferedImage imgOne, imgtWO = null;
            Image myImgOne, myImgTwo = null;
            if (type.getSelectedItem() == "Chocolate") {
                Database.displaySweetInfo("DarkC", "WhiteC");
                imgOne = ImageIO.read(getClass().getResource("/CandyImages/Pomegranate.jpg"));
                imgtWO = ImageIO.read(getClass().getResource("/CandyImages/Oreo.jpg"));
                myImgOne = imgOne.getScaledInstance(img1.getWidth(), img1.getHeight(), Image.SCALE_DEFAULT);
                img1.setIcon(new ImageIcon((myImgOne)));
                myImgTwo = imgtWO.getScaledInstance(img2.getWidth(), img2.getHeight(), Image.SCALE_DEFAULT);
                img2.setIcon(new ImageIcon((myImgTwo)));

            } else if (type.getSelectedItem() == "Gummy Candy") {
                Database.displaySweetInfo("SourG", "BearG");
                imgOne = ImageIO.read(getClass().getResource("/CandyImages/worm.jpg"));
                imgtWO = ImageIO.read(getClass().getResource("/CandyImages/bear.jpg"));
                myImgOne = imgOne.getScaledInstance(img1.getWidth(), img1.getHeight(), Image.SCALE_DEFAULT);
                img1.setIcon(new ImageIcon((myImgOne)));
                myImgTwo = imgtWO.getScaledInstance(img2.getWidth(), img2.getHeight(), Image.SCALE_DEFAULT);
                img2.setIcon(new ImageIcon((myImgTwo)));
            }
        } catch (Exception ex) {
            Logger.getLogger(Shop.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_typeActionPerformed

